class Point:
    # Конструктор - вызывается автоматически, при создании объекта из класса
    def __init__(self, x, y):
        self.x = x
        self.y = y


# Создадим несколько объектов
point1 = Point(10, -8)
point2 = Point(12, 5)

print("point1.x = ", point1.x)
print("point1.y = ", point1.y)

print("point2.x = ", point2.x)
print("point2.y = ", point2.y)
